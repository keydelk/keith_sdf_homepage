     <h2 id="description">Description</h2>
     <img src="../images/Keydel_Stollen.jpg" alt="Two loaves of Stollen on a baking sheet">
     <p>
      <a href="https://en.wikipedia.org/wiki/Stollen">Stollen</a> is a traditional
      German Christmas bread filled with nuts and candied fruit.
      This recipe was given to my wife and me by my great-aunt Jane Keydel.
      Family lore is that this recipe came from my great-great-great grandparents
      Karl Friedrick and Emilie-Bertha Keydel when they emmigrated from Leipzig,
      Germany in 1880. This recipe takes a while to make, and the first couple steps
      should be done the night-before baking. It also makes a lot, so there is
      plenty to give out as gifts.
     </p>
     <p>
      Makes 13 loaves.
     </p>

     <br>
     <h3 id="ingredients">Ingredients</h3>
     <ul>
      <li>1 lb seeded raisins</li>
      <li>1 lb golden raisins</li>
      <li>1 lb currants</li>
      <li>¾ lb chopped citron</li>
      <li>¼ lb chopped candied green and red cherries</li>
      <li>2.5 lbs slivered almonds</li>
      <li>12 c bread flour</li>
      <li>3.5 c sugar</li>
      <li>10 - 12 packages yeast</li>
      <li>1 qt milk</li>
      <li>3 lbs butter</li>
      <li>6 eggs</li>
      <li>½ c brandy</li>
      <li>1½ tbs salt</li>
      <li>1 tsp cardamom</li>
      <li>1 tsp mace</li>
      <li>1 lemon rind</li>
      <li>confectioner's sugar frosting</li>
      <li>red and green colored sugar</li>
     </ul>

     <br>
     <h3 id="directions">Directions</h3>
     <ol>
      <li>
        The night before baking, mix together: raisins (seeded and golden), 
        currants, chopped citron, candied cherries, and 3 c bread flour, mix so 
        fruit won't  stick together.
      </li>
      <li>
        Coarsly chop 1.5 lbs slivered almonds in a food processor. Mix all 
        almonds into fruit mixture.
      </li>
      <li>
        In the morning, in a large bowl, mix together: 5 c bread flour, 
        1.5 c sugar, and 10-12 packages yeast.
      </li>
      <li>
        Warm 1 qt milk and 1 c water to 130º F, stir together and place in a 
        warm place for sponge to rise - approximately ½ hour
      </li>
      <li>
        After rising add: 2¼ lb butter (somewhat softened), 6 eggs, 1½ tbs salt, 
        ½ c brandy, 2 c sugar, 1 tsp cardamom, 1 tsp mace and the grated rind of 
        1 lemon. Mix together with beater till smooth.
      </li>
      <li>
        Add about 4 c bread flour, mixing with beater.
      </li>
      <li>
        Mix in fruit/nut/flour mixture with spoon until fairly well blended.
      </li>
      <li>
        Pour some bread flour (~3 c) on table and knead lightly into about 
        13 parts and work each one only till it holds together. Each part 
        should weigh about 1½ lbs.
      </li>
      <li>
        Place in containers in a warm place to rise (till about double).
      </li>
      <li>
        Butter pans for loaves
      </li>
      <li>
        When risen, knead each loaf till it's very pliable (can combine two 
        parts for kneading and the divide to make sure the fruit is evenly 
        divided). A lot of flour is incorporated here, but the loaves should 
        not be heavy and dense, it should still be light to handle.
      </li>
      <li>
        Roll each loaf into a rounded shape and fold double.
      </li>
      <li>
        Place in baking pans (or baking sheets) to rise until double.
      </li>
      <li>
        Place loaves in a cool oven and heat the oven to 300º - 350º F
      </li>
      <li>
        Check after 15 min, it will take longer. (Usually it takes around 30 minutes).
      </li>
      <li>
        Brush loaves with ½ lb melted butter as soon as they come out of the oven
      </li>
      <li>
        Spread with confectioner's sugar frosting and sprinkle with colored (red and green) sugar.
      </li>
     </ol>

