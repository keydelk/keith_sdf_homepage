    <h2 id="description">Description</h2>
    <p>
      I started making chili back in high-school, but never really had a recipe until recently.
      I would just add ground beef, onions, tomatoes, peppers, kidney beans and spices until 
      it tasted good to me. But my family likes it a bit milder than I do, so I started measuring
      ingredients get a more consistent outcome. I like my chili rather busy with lots of flavors,
      so I add both fresh bell peppers and beans. Goes well with cornbread and lots of toppings
    </p>

    <br>
    <h3 id="ingredients">Ingredients</h3>
    <ul>
      <li>1 lb ground beef</li>
      <li>1 medium onion - chopped</li>
      <li>2 cloves garlic - diced (or more to taste)</li>
      <li>1 or 2 bell peppers - diced (any color)</li>
      <li>1 habenero, jalapeno or other chili pepper - finely diced (optional)</li>
      <li>1 tbsp canola oil</li>
      <li>1 28 oz can of crushed tomatoes</li>
      <li>1 14.5 oz can of fire-roasted diced tomatoes (or diced tomatoes with chilies)</li>
      <li>1 15.5 oz can of kidney beans</li>
      <li>1 tbsp chili powder (or to taste)</li>
      <li>2 tbsp smoked paparika (or to taste)</li>
      <li>salt</li>
      <li>black pepper</li>
      <li>shredded cheese (cheddar or mexican blend) for serving</li>
      <li>sour cream for serving</li>
      <li>Fritos or corn-chips for serving</li>
    </ul>

    <br>
    <h3 id="directions">Directions</h3>
    <ol>
      <li>In a dutch oven or large pot, heat 1 tbsp of oil over medium heat and add ground beef</li>
      <li>Cook until browned. While cooking, season with salt, pepper, and about 1 tsp chili powder.</li>
      <li>Add onion and garlic. Cook until onion is translucent.</li>
      <li>Add crushed tomatoes and fire-roasted tomatoes. Bring to a low boil, stirring often</li>
      <li>Reduce heat and let simmer for about 5 minutes</li>
      <li>Add chili powder, smoked paparika and chilies. Taste to make sure it doesn't get too spicy.</li>
      <li>Add kidney beans and bell peppers. Let simmer for about 5 minutes more.</li>
      <li>Serve with shredded cheese, sour cream, and/or Fritos as desired</li>
    </ol>

